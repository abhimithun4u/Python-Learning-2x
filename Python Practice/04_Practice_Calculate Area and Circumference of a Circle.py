# Program: Calculate Area and Circumference of a Circle
pi = 3.14
r = float(input("Enter the value of radius : \n"))
print(r)
area = float(pi*r*r)
circumference = float(2*pi*r)
print("The Area of the Circle with radius",r," is ",area)
print("The Circumference of the Circle with radius",r," is ",circumference)
